const _ = require('lodash')

/**
 * Pluralize a string
 *
 * @param {*} value
 * @returns {*}
 */
function pluralize (value) {
  const args = _.slice(_.toArray(arguments), 1)
  return args.length > 1
    ? (args[value % 10 - 1] || args[args.length - 1])
    : (args[0] + (value === 1 ? '' : 's'))
}

export default pluralize
